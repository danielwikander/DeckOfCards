/**
 * Using an enum for the ranks
 * (and suits, see Suit.java)
 * allows us to rank the suits after
 * their ordinal value in the enum.
 * eg. ACE = 1, TWO = 2 etc.
 */
enum Rank {
    ACE,
    TWO,
    THREE,
    FOUR,
    FIVE,
    SIX,
    SEVEN,
    EIGHT,
    NINE,
    TEN,
    JACK,
    QUEEN,
    KING
}
